# Solicita uma string como entrada do usuário
texto = input("Digite uma string: ")

# Solicita um número inteiro como entrada do usuário
numero = int(input("Digite um número inteiro: "))

# Repete a string o número de vezes informado e exibe o resultado
resultado = (texto + " ") * numero
print(f"A string repetida {numero} vezes é: {resultado} ")
